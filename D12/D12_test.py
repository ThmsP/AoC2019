from D12 import iterations

def test_10iterations():
  moons = {'m1' : [[-1, 0, 2],[0, 0, 0]],
           'm2' : [[2, -10, -7],[0, 0, 0]],
           'm3' : [[4, -8, 8],[0, 0, 0]],
           'm4' : [[3, 5, -1],[0, 0, 0]]}
  assert iterations(moons, 10) == {'m1' : [[2, 1, -3],[-3, -2, 1]],
                                   'm2' : [[1, -8, 0],[-1, 1, 3]],
                                   'm3' : [[3, -6, 1],[3, 2, -3]],
                                   'm4' : [[2, 0, 4],[1, -1, -1]]}